/**
 * =====================================================================
 * JavaScript Basic: 변수와 상수 & 네이밍 컨벤션 - Executable Note
 * =====================================================================
 * ✅ 핵심 요약
 * 1. let: 변하는 값 (Variable). 재할당 가능.
 * 2. const: 변하지 않는 값 (Constant). 재할당 불가. (기본으로 이걸 쓰세요! ⭐)
 * 3. var: 옛날 방식. 호이스팅 문제로 버그의 원흉. 절대 쓰지 말 것. 🛡️
 *
 * 🔥 실무 네이밍 가이드
 * - camelCase: 변수, 함수 이름 (예: myAge, getUser)
 * - PascalCase: 클래스, 리액트 컴포넌트 (예: UserProfile, App)
 * - UPPER_CASE: 절대 안 변하는 설정값 (예: API_KEY, MAX_COUNT)
 */

console.clear?.();

const line = () => console.log('-------------------------------------');

// =====================================================================
// 1) 변수 (let) - 값이 바뀔 수 있음
// =====================================================================
{
   console.log('1. 변수 (Variable)');

   let age = 28;
   console.log('올해 나이:', age);

   age = 30; // ✅ 재할당 가능 (값이 바뀜)
   console.log('2년 뒤 나이:', age);

   // let age = 31; // ❌ 재선언은 불가능 (같은 이름 또 쓰기 X)
}

line();

// =====================================================================
// 2) 상수 (const) - 값이 절대 안 바뀜 (권장 ⭐)
// =====================================================================
{
   console.log('2. 상수 (Constant)');

   const name = '신재준';
   console.log('이름:', name);

   // name = '김철수'; // ❌ Error: Assignment to constant variable.
   // const는 태어날 때(선언) 반드시 값을 넣어줘야 함.

   /**
    * 💡 실무 Tip:
    * "이거 값이 바뀔까?" 고민되면 무조건 const로 시작하세요.
    * 나중에 에러 나면 그때 let으로 바꿔도 늦지 않습니다. (안전 제일!)
    */
}

line();

// =====================================================================
// 3) 변수 명명 규칙 (규칙 = 에러 남)
// =====================================================================
{
   // 1. $, _ 를 제외한 특수문자 사용 불가
   let $bonus = 100;
   let _private = 'secret';
   // let user-name = 'test'; // ❌ (하이픈 안됨)

   // 2. 숫자로 시작 불가
   // let 1stPlace = 'gold'; // ❌
   let place1st = 'gold'; // ✅

   // 3. 예약어 사용 불가 (언어 자체 키워드)
   // let if = 10; // ❌
   // let const = 20; // ❌
}

// =====================================================================
// 4) 변수 명명 가이드 (약속 = 에러는 안 나지만 욕 먹음 😅)
// =====================================================================
{
   console.log('4. 실무 네이밍 컨벤션');

   // ✅ 카멜 케이스 (camelCase)
   // 변수나 함수는 소문자로 시작, 단어 바뀔 때 대문자
   let userName = 'JaeJun';
   let isLoggedIn = true; // (Boolean은 보통 is, has로 시작)

   // ✅ 파스칼 케이스 (PascalCase)
   // 클래스나 **React 컴포넌트**는 대문자로 시작
   // class UserInfo {}
   // function MyComponent() {}

   // ✅ 스네이크 케이스 (snake_case)
   // 상수나 DB 컬럼명에서 가끔 쓰임 (JS 변수로는 잘 안 씀)
   const MAX_COUNT = 999; // (완전 고정된 상수)

   console.log(`User: ${userName}, LoggedIn: ${isLoggedIn}`);
}
